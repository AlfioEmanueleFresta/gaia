        <h2><i class="icon-info-sign"></i> Il progetto Gaia</h2>
        <hr />
        <h3>La storia</h3>
        <p>Gaia nasce nell'estate del 2012 con l'idea di una profonda rivoluzione in Croce Rossa, che mostri il dinamismo, la forza e la giovinezza dell'associazione.</p>
        <p>Crediamo in una Croce Rossa Italiana che sa muoversi velocemente, più trasparente ed aperta a tutti.</p>
        <p>&nbsp;</p>
        
        <h3>L'obiettivo</h3>
        <p>Vogliamo ottenere un sistema informativo efficiente e di facile consultazione.</p>
        <p>&nbsp;</p>
            
        <h3>Punti chiave del progetto</h3>  
        <p>
            <ul>
                <li><strong>Efficienza</strong>: Bisogna cavare il potenziale inespresso di Croce Rossa.</li>
                <li><strong>Semplicità</strong>: Il sistema deve essere semplice per chiunque.</li>
                <li><strong>Trasparenza</strong>: Croce Ross aè aperta e trapsarente a tutti.</li>
            </ul>
        </p>
        <p>&nbsp;</p>
        
        <h3>Il nome</h3>

        <p>
            <blockquote>

                <p>
                  G.A.I.A. è l'acronimo di "Gestione Avanzata ed Integrata dell'Anagrafica"
                  ed identifica la web-application sviluppata dai volontari della Croce Rossa di
                  Catania per semplificare le quotidiane attività di gestione del personale e dei
                  mezzi dell'associazione.
                </p>
                <p>
                  Gaia è anche il nome di un pianeta immaginario, frutto della fantasia di Isaac Asimov, in
                  cui grazie alla perfetta integrazione di ognuna della sue parti l'intero sistema
                  si mantiene in un perfetto equilibrio con un costante flusso di comunicazione ed una
                  stretta interdipendenza.
                </p>
                  <small>cit. Alfio Musmarra</small>
            </blockquote>
         </p>
        <p>&nbsp;</p>
        
